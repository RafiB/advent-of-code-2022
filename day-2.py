OPP_ROCK = 'A'
OPP_PAPER = 'B'
OPP_SCISSORS = 'C'

LOSE = 'X'
DRAW = 'Y'
WIN = 'Z'

RESULT_STRATEGY = {
    LOSE: {
        OPP_SCISSORS: PAPER,
        OPP_PAPER: ROCK,
        OPP_ROCK: SCISSORS,
    },
    DRAW: {
        OPP_SCISSORS: SCISSORS,
        OPP_PAPER: PAPER,
        OPP_ROCK: ROCK,
    },
    WIN: {
        OPP_SCISSORS: ROCK,
        OPP_PAPER: SCISSORS,
        OPP_ROCK: PAPER,
    }
}

PLAY_SCORE = {
    ROCK: 1,
    PAPER: 2,
    SCISSORS: 3
}

RESULT_SCORE = {
    (False, False, True): 0,
    (False, True, False): 3,
    (True, False, False): 6,
}

def solve(puzzle_input):
    score = 0
    for line in puzzle_input.split("\n"):
        opp, res = line.split(" ")

        me = RESULT_STRATEGY[res][opp]

        i_win = me == RESULT_STRATEGY[WIN][opp]
        i_draw = me == RESULT_STRATEGY[DRAW][opp]
        i_lose = me == RESULT_STRATEGY[LOSE][opp]

        play_score = PLAY_SCORE[me]
        result_score = RESULT_SCORE[(i_win, i_draw, i_lose)]
        score += play_score + result_score

    return score

